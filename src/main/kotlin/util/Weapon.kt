package util

/**
 * Contains all the available weapons:
 *  two types for archers(bowmen and crossbowmen)
 *  two types for warriors(swordmen and axemen)
 */
enum class Weapon {
    // default ones
    BOW,
    CROSSBOW,
    SWORD,
    AXE,

    // superior ones
    COMPOSITE_BOW,
    COMPOSITE_CROSSBOW,
    COMPOSITE_SWORD,
    COMPOSITE_AXE
}